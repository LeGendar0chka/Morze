def main() -> str: 
    while True:
        n = int(input())
        if n == 1:
            print("".join(code(input())))
        else:
            print("".join(decode(input())))


def decode(v) -> list[str]:
    b = []
    for i in v:
        b.append(revmorse.get(i))
    return b


def code(a) -> list[str]:
    b = []
    for i in a:
        b.append(morse.get(i))
    return b
